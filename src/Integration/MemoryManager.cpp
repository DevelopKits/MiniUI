#include "MemoryManager.h"
#include <stdlib.h>
#include <iostream>

namespace Integration
{
	///////////////////////////////////////////////////////////////////////////
	void* MemoryManager::Allocate ( unsigned int size )
	///////////////////////////////////////////////////////////////////////////
	{
		return malloc ( size );
	}

	///////////////////////////////////////////////////////////////////////////
	void MemoryManager::Deallocate ( void* pData )
	///////////////////////////////////////////////////////////////////////////
	{
		free ( pData );
	}

	///////////////////////////////////////////////////////////////////////////
	void* MemoryManager::Reallocate ( void *pData, unsigned int newSize )
	///////////////////////////////////////////////////////////////////////////
	{
		return realloc ( pData, newSize );
	}
}
